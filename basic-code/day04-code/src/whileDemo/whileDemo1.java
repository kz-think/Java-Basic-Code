package whileDemo;

public class whileDemo1 {
    public static void main(String[] args) {
        //while的格式
        /*
        初始化语句;
        while (条件判断语句) {
            循环体语句;
            条件控制语句;
        }
        */

        int i = 0;
        while (i<=5) {
            System.out.println(i);
            i++;
        }

        //for和while的区别
        //for循环中，所有条件都很清晰
    }
}
